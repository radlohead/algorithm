const app = require('./app')

it('단지번호 붙이기', () => {
    expect(
        app(7, [
            '0110100',
            '0110101',
            '1110101',
            '0000111',
            '0100000',
            '0111110',
            '0111000'
        ])
    ).toEqual([3, [7, 8, 9]])
    expect(
        app(5, ['01101', '01101', '10100', '00001', '01000', '01000', '01110'])
    ).toEqual([5, [1, 1, 2, 5, 5]])
})
